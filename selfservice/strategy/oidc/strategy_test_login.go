// PopulateTestLoginFlow renders a single-provider submit button for an
// admin-created test login flow. Implements login.TestStrategy.
//
// Unlike PopulateLoginMethodFirstFactor, which renders one button per
// configured provider, a test login flow is pre-scoped to a single provider
// at creation time and renders only that button. The flow's own CSRF bearer
// token is pre-generated by the admin-create handler and baked into the UI
// so submit authorization works without a browser cookie.
func (s *Strategy) PopulateTestLoginFlow(r *http.Request, f *login.Flow, providerID string) error {
	conf, err := s.Config(r.Context())
	if err != nil {
		return errors.WithStack(err)
	}

	var cfg *Configuration
	for i := range conf.Providers {
		if conf.Providers[i].ID == providerID {
			cfg = &conf.Providers[i]
			break
		}
	}
	if cfg == nil {
		return errors.WithStack(herodot.ErrNotFound().WithReasonf("OpenID Connect provider %q is not configured", providerID))
	}

	f.UI.SetCSRF(f.CSRFToken)
	AddProvider(f.UI, cfg.ID, text.NewInfoLoginWith(cmp.Or(cfg.Label, cfg.ID), cfg.ID), s.ID())
	return nil
}

// processTestLogin captures the outcome of an OIDC test login flow's provider
// round-trip into a DebugPayload on f.TestContext. No identity is persisted
// and no session is issued. Runtime errors in mapping or schema validation
// are recorded on DebugPayload.Error so the admin UI can render a full debug
// view. Only infrastructure failures cause this function to return an error.
func (s *Strategy) processTestLogin(
	ctx context.Context,
	w http.ResponseWriter,
	r *http.Request,
	f *login.Flow,
	claims *claims.Claims,
	provider Provider,
) error {
	if f.State != flow.StateChooseMethod {
		return errors.WithStack(herodot.ErrConflict().WithReason("test login flow already captured"))
	}
	if err := f.LoadTestContext(); err != nil {
		return err
	}
	if f.TestContext == nil {
		return errors.WithStack(herodot.ErrInternalServerError().WithReason("flow is marked as a test flow but has no test context"))
	}

	dp := &login.DebugPayload{
		JsonnetMapperURL: provider.Config().Mapper,
	}
	// Raw bearer tokens are intentionally not captured. The GET endpoint is
	// CSRF-bearer-authorized, which would make a 24h window for live
	// credentials an unnecessary risk. The id_token claims below give admins
	// enough to validate an OIDC integration.

	if claims == nil {
		dp.Error = &login.StepError{Step: "claims_decode", Reason: "missing_claims", Message: "OIDC provider returned no claims"}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}

	claimsMap, err := claimsToMap(claims)
	if err != nil {
		dp.Error = &login.StepError{Step: "claims_decode", Reason: "marshal_failed", Message: err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}
	dp.IDTokenClaims = claimsMap

	evaluated, claimsJSON, err := s.EvaluateClaimsMapper(ctx, claims, provider, nil)
	if err != nil {
		dp.Error = &login.StepError{Step: "jsonnet_run", Reason: "evaluation_failed", Message: err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}

	if len(claimsJSON) > 0 {
		var in map[string]any
		if err := json.Unmarshal(claimsJSON, &in); err == nil {
			dp.JsonnetInput = in
		}
	}

	var output map[string]any
	if err := json.Unmarshal([]byte(evaluated), &output); err != nil {
		dp.Error = &login.StepError{Step: "jsonnet_run", Reason: "invalid_output", Message: "Jsonnet mapper did not return a JSON object: " + err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}
	dp.JsonnetOutput = output

	// Build an identity and run schema validation without persisting.
	i := identity.NewIdentity(s.d.Config().DefaultIdentityTraitsSchemaID(ctx))
	if err := s.setTraits(provider, nil, evaluated, i); err != nil {
		dp.Error = &login.StepError{Step: "jsonnet_run", Reason: "invalid_traits", Message: err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}
	if err := s.setMetadata(evaluated, i, PublicMetadata); err != nil {
		dp.Error = &login.StepError{Step: "jsonnet_run", Reason: "invalid_metadata_public", Message: err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}
	if err := s.setMetadata(evaluated, i, AdminMetadata); err != nil {
		dp.Error = &login.StepError{Step: "jsonnet_run", Reason: "invalid_metadata_admin", Message: err.Error()}
		return s.finishTestLogin(ctx, w, r, f, dp)
	}

	if err := s.d.IdentityValidator().Validate(ctx, i); err != nil {
		dp.SchemaValidationErrors = extractSchemaValidationErrorsForLogin(err)
		dp.Error = &login.StepError{Step: "schema_validate", Reason: "traits_invalid", Message: err.Error()}
	}

	return s.finishTestLogin(ctx, w, r, f, dp)
}

// finishTestLoginWithError converts a callback-side error into a DebugPayload
// tagged with the step and finishes the flow through finishTestLogin.
func (s *Strategy) finishTestLoginWithError(ctx context.Context, w http.ResponseWriter, r *http.Request, f *login.Flow, err error) error {
	e := oidcerr.Unwrap(err)
	dp := &login.DebugPayload{
		Error: &login.StepError{Step: string(e.Step), Reason: e.Step.Reason(), Message: err.Error()},
	}
	return s.finishTestLogin(ctx, w, r, f, dp)
}

// finishTestLogin persists the DebugPayload into the flow's test context,
// marks the flow as completed, and redirects to the admin test UI.
func (s *Strategy) finishTestLogin(ctx context.Context, w http.ResponseWriter, r *http.Request, f *login.Flow, dp *login.DebugPayload) error {
	if f.TestContext == nil {
		if err := f.LoadTestContext(); err != nil {
			return err
		}
	}
	if f.TestContext != nil && f.TestContext.DebugPayload != nil {
		http.Redirect(w, r, testLoginReturnURL(ctx, s, f), http.StatusSeeOther)
		return nil
	}

	tc := &login.TestContext{DebugPayload: dp}
	if f.TestContext != nil {
		tc.ProviderID = f.TestContext.ProviderID
	}
	if err := f.SetTestContext(tc); err != nil {
		return err
	}
	f.State = flow.StatePassedChallenge

	// Rotate the CSRF cookie and pin the new token onto the flow. Subsequent
	// reads/deletes of the captured DebugPayload (which may contain PII) only
	// succeed for the browser that completed the OIDC round trip — its cookie
	// holds the new token, and the GET/DELETE handlers verify the cookie
	// against f.CSRFToken via nosurf.VerifyToken.
	f.CSRFToken = s.d.CSRFHandler().RegenerateToken(w, r)
	if err := s.d.LoginFlowPersister().UpdateLoginFlow(ctx, f); err != nil {
		return errors.WithStack(err)
	}

	http.Redirect(w, r, testLoginReturnURL(ctx, s, f), http.StatusSeeOther)
	return nil
}

// testLoginReturnURL builds the admin test UI URL the browser lands on after
// a test login flow callback completes. Derived from the login UI host since
// test flows don't have a dedicated config entry.
func testLoginReturnURL(ctx context.Context, s *Strategy, f *login.Flow) string {
	base := s.d.Config().SelfServiceFlowLoginUI(ctx)
	target := &url.URL{Scheme: base.Scheme, Host: base.Host, Path: "/session/test-oidc"}
	q := target.Query()
	q.Set("flow", f.ID.String())
	target.RawQuery = q.Encode()
	return target.String()
}

func extractSchemaValidationErrorsForLogin(err error) []login.SchemaValidationError {
	if err == nil {
		return nil
	}
	if wrapped, ok := stderrors.AsType[*schema.ValidationError](err); ok && wrapped.ValidationError != nil {
		if out := collectLeafValidationErrorsForLogin(wrapped.ValidationError); len(out) > 0 {
			return out
		}
	}
	if raw, ok := stderrors.AsType[*jsonschema.ValidationError](err); ok {
		if out := collectLeafValidationErrorsForLogin(raw); len(out) > 0 {
			return out
		}
	}
	return []login.SchemaValidationError{{Path: "", Message: err.Error()}}
}

func collectLeafValidationErrorsForLogin(v *jsonschema.ValidationError) []login.SchemaValidationError {
	if v == nil {
		return nil
	}
	if len(v.Causes) == 0 {
		return []login.SchemaValidationError{{
			Path:    strings.TrimPrefix(v.InstancePtr, "#"),
			Message: v.Message,
		}}
	}
	var out []login.SchemaValidationError
	for _, c := range v.Causes {
		out = append(out, collectLeafValidationErrorsForLogin(c)...)
	}
	return out
}

// claimsToMap serializes OIDC id_token claims to a generic map so they can be
// recorded verbatim on DebugPayload.IDTokenClaims for the admin debug view.
func claimsToMap(claims *claims.Claims) (map[string]any, error) {
	raw, err := json.Marshal(claims)
	if err != nil {
		return nil, errors.WithStack(err)
	}
	var m map[string]any
	if err := json.Unmarshal(raw, &m); err != nil {
		return nil, errors.WithStack(err)
	}
	return m, nil
}
